import numpy as np

def load_balancing(size,rank,n):

    # Load balancing
    ini = np.zeros((size),dtype=int)
    end = np.zeros((size),dtype=int)
    splitsize = 1.0/size*n
    for i in xrange(size):
        ini[i] = int(round(i*splitsize))
        end[i] = int(round((i+1)*splitsize))
    start = ini[rank]
    stop = end[rank]

    return(start,stop)
